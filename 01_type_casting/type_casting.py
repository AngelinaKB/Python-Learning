# Integer to Float
x = 5
y = float(x)
print(y)       # Output: 5.0

# Float to Integer (removes decimal part)
a = 3.9
b = int(a)
print(b)       # Output: 3

# Integer to String
num = 42
text = str(num)
print(text)    # Output: "42"

# String to Integer
str_num = "100"
int_num = int(str_num)
print(int_num) # Output: 100

# String to Float
str_float = "3.14"
float_num = float(str_float)
print(float_num)  # Output: 3.14
