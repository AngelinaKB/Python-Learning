# Variable assignment
name = "Alice"           # String variable
age = 25                 # Integer variable
height = 5.7             # Float variable
is_student = True        # Boolean variable

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is a student:", is_student)

# Updating a variable
age = age + 1  # age is now 26
print("Updated Age:", age)
